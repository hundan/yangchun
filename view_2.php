<section class="container view_box">
    <h4 class="title_blue title_top">仙湖</h4>
    <p>八甲仙湖位于广东省阳春市八甲镇鹅凰嶂的五指峰下，于1971年建成水库，蓄水发电。
        该湖坝顶标高521米，湖面积愈100万平方米，水面宽处1900米，湖水最深处38米，蓄水2230万立方米，又名仙家垌水库。
        湖心数座小岛，恰似神话中的“逢瀛之岛”。
        </p>
    <div class="row">
        <div class="col-lg-7 col-md-7 col-sm-7 col-xs-12">
            <p>仙湖有四美：</p>
            <p>一是山美，环湖皆山，层峦叠嶂；</p>
            <p>二是水美，仙湖处于丛山怀抱之中，集雨面积数十平方公里，全为层林覆盖，下雨不着土，入湖无浊水；</p>
            <p>三是气候美，湖区最高气温不超过30℃，比山下约低5℃--6℃，气候凉爽宜人，适合人们度假消夏。</p>
            <p>四是环境美，驱车登山，峰迥路转，经过三坳九弯，便见湖光粼粼，碧波千顷，几处小岛，散布湖中，有若蓬瀛，及至下车登艇，绕湖徐行，顿觉凉风拂面，暑气全消。
